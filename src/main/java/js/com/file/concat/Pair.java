package js.com.file.concat;

public class Pair {

	int beginIndex;
	int endIndex;
	int size;
	
	int beginTotalLine;
	int endTotalLine;
	int lineSize;
	
	
	public int getBeginIndex() {
		return beginIndex;
	}
	public void setBeginIndex(int beginIndex) {
		this.beginIndex = beginIndex;
	}
	public int getEndIndex() {
		return endIndex;
	}
	public void setEndIndex(int endIndex) {
		this.endIndex = endIndex;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public int getBeginTotalLine() {
		return beginTotalLine;
	}
	public void setBeginTotalLine(int beginTotalLine) {
		this.beginTotalLine = beginTotalLine;
	}
	public int getEndTotalLine() {
		return endTotalLine;
	}
	public void setEndTotalLine(int endTotalLine) {
		this.endTotalLine = endTotalLine;
	}
	public int getLineSize() {
		return lineSize;
	}
	public void setLineSize(int lineSize) {
		this.lineSize = lineSize;
	}
	
	
	
	
	
	
	
}
